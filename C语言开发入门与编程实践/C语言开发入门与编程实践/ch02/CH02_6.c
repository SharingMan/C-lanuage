#include<stdio.h>
#include<stdlib.h>

int main()
{	
	int A=21,B=6;
	/*算术操作符的各种运算*/
    printf("A=21,B=6\n");
    printf("A+B=%d\n",A+B);
    printf("A-B=%d\n",A-B);
    printf("A*B=%d\n",A*B);
    printf("A/B=%d\n",A/B);
    printf("A%%B=%d\n",A%B);/* 重复%字符,才会打印出*/ 
    system("pause");
    return 0;
}
